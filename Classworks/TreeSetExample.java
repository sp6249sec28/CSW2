import java.util.TreeSet;
public class TreeSetExample {
    public static void main(String[] args) {
        TreeSet<String> tset = new TreeSet();
        tset.add("Efg");
        tset.add("stores");
        tset.add("free");
        tset.add("movies");
        tset.add("cricket");
        tset.add("politics");
        System.out.println(tset);

        //TreeSet gives the output in sorted manner.
    }
}
